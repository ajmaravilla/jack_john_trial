// *** Dependencies
// =============================================================
var mongoose = require('mongoose');

// =============================================================
// *** Create Schema
// =============================================================
var Schema = mongoose.Schema;

var newReply = new Schema ({
	author: {
		type: String,
		/*required: true*/
	},
	text: {
		type: String,
		minlength: 1
	},
	createdAt: {
	    type: Date,
	    default: Date.now
  	}
});


// =============================================================
// *** Create Reply Model
// =============================================================
var Reply = mongoose.model('Reply', newReply);

// =============================================================
// *** Export the Reply Model
// =============================================================
module.exports = Reply;